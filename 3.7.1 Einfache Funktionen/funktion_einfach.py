# Definition der FUnktion
def stern():
    print("_________________")
    print("*** Trennung ****")
    print("_________________")

# Hauptprogramm
x = 12
y = 5

stern()                         # 1. Aufruf
print("x =", x, ", y =", y)
stern()                         # 2. Aufruf
print("x + y =", x + y)
stern()                         # 3. Aufruf
print("x - y =", x - y)
stern()                         # 4. Aufruf
